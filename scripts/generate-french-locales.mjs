function buildLocale(source, translations) {
  const result = {};
  const missing = [];
  for (const key of Object.keys(source)) {
    if (key === '_comment') {
      result[key] = translations[key] ?? source[key];
      continue;
    }
    if (translations[key] !== undefined) {
      result[key] = translations[key];
    } else {
      missing.push(key);
      result[key] = source[key];
    }
  }
  return { result, missing };
}

const webBuilt = buildLocale(webEn, webFr);
const panelBuilt = buildLocale(panelEn, panelFr);

if (webBuilt.missing.length) {
  console.warn('Web keys missing French translation:', webBuilt.missing.join(', '));
}
if (panelBuilt.missing.length) {
  console.warn('Panel keys missing French translation:', panelBuilt.missing.join(', '));
}

const outputs = [
  [path.join(root, 'src/utils/locale/fr.json'), webBuilt.result],
  [path.join(root, 'storage/app/public/language/fr.json'), webBuilt.result],
  [path.join(root, 'resources/lang/fr.json'), panelBuilt.result],
];

for (const [filePath, data] of outputs) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, JSON.stringify(data, null, 4) + '\n', 'utf8');
  console.log('Written:', filePath);
}

